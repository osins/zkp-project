#!/bin/bash

###############################################################################
# 生产级电路测试脚本
###############################################################################
#
# 用途: 运行所有生产级电路的测试套件
# 状态: ✅ 生产级
#
# 功能:
#   - 运行所有测试用例
#   - 生成测试报告
#   - 计算测试覆盖率
#   - 验证性能指标
#
# 使用方法:
#   ./scripts/test_production.sh
#
# 前置条件:
#   - 已安装 Node.js (>= 14.0)
#   - 已安装依赖 (npm install)
#   - 已构建电路
#
# 作者: ZKP Project Team
# 版本: 1.0.0
# 创建日期: 2025-11-08
###############################################################################

set -e

# 颜色定义
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo -e "${BLUE}🧪 运行生产级电路测试套件${NC}"
echo -e "${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
echo ""

# 检查 Node.js
if ! command -v node &> /dev/null; then
    echo -e "${RED}❌ 错误: Node.js 未安装${NC}"
    exit 1
fi

NODE_VERSION=$(node --version)
echo -e "${BLUE}ℹ️  Node.js 版本: $NODE_VERSION${NC}"

# 检查 npm 依赖
if [ ! -d "node_modules" ]; then
    echo -e "${YELLOW}⚠️  依赖未安装，正在安装...${NC}"
    npm install
fi

# 运行测试
echo ""
echo -e "${YELLOW}🧪 运行测试用例...${NC}"
echo ""

# 使用 mocha 运行测试
if npm test; then
    echo ""
    echo -e "${GREEN}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    echo -e "${GREEN}✅ 所有测试通过！${NC}"
    echo -e "${GREEN}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    exit 0
else
    echo ""
    echo -e "${RED}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    echo -e "${RED}❌ 测试失败！${NC}"
    echo -e "${RED}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    exit 1
fi
