const hre = require("hardhat");
const fs = require("fs");
const path = require("path");

async function main() {
  console.log("üöÄ Deploying ZKP Contracts...\n");

  const [deployer] = await hre.ethers.getSigners();
  console.log("üìù Deploying with account:", deployer.address);

  const balance = await hre.ethers.provider.getBalance(deployer.address);
  console.log("üí∞ Account balance:", hre.ethers.formatEther(balance), "ETH\n");

  // 1. ÈÉ®ÁΩ≤ Verifier ÂêàÁ∫¶
  console.log("1Ô∏è‚É£  Deploying Groth16Verifier...");
  const Verifier = await hre.ethers.getContractFactory("Groth16Verifier");
  const verifier = await Verifier.deploy();
  await verifier.waitForDeployment();
  const verifierAddress = await verifier.getAddress();
  console.log("‚úÖ Groth16Verifier deployed to:", verifierAddress);

  // 2. ÈÉ®ÁΩ≤Â∫îÁî®ÂêàÁ∫¶
  console.log("\n2Ô∏è‚É£  Deploying ZKPApplication...");
  const ZKPApp = await hre.ethers.getContractFactory("ZKPApplication");
  const zkpApp = await ZKPApp.deploy(verifierAddress);
  await zkpApp.waitForDeployment();
  const zkpAppAddress = await zkpApp.getAddress();
  console.log("‚úÖ ZKPApplication deployed to:", zkpAppAddress);

  // 3. ‰øùÂ≠òÈÉ®ÁΩ≤‰ø°ÊÅØ
  const deploymentInfo = {
    network: hre.network.name,
    deployer: deployer.address,
    timestamp: new Date().toISOString(),
    contracts: {
      Groth16Verifier: verifierAddress,
      ZKPApplication: zkpAppAddress
    }
  };

  const deploymentPath = path.join(__dirname, "../deployments.json");
  fs.writeFileSync(deploymentPath, JSON.stringify(deploymentInfo, null, 2));
  console.log("\nüíæ Deployment info saved to:", deploymentPath);

  // 4. È™åËØÅÈÉ®ÁΩ≤
  console.log("\nüîç Verifying deployment...");
  const storedVerifier = await zkpApp.verifier();
  console.log("   ZKPApplication.verifier():", storedVerifier);
  console.log("   Match:", storedVerifier === verifierAddress ? "‚úÖ" : "‚ùå");

  // 5. ËæìÂá∫ÊëòË¶Å
  console.log("\nüìä Deployment Summary:");
  console.log("‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ");
  console.log(`Network:          ${hre.network.name}`);
  console.log(`Deployer:         ${deployer.address}`);
  console.log(`Verifier:         ${verifierAddress}`);
  console.log(`ZKP Application:  ${zkpAppAddress}`);
  console.log("‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ");

  // 6. ÁîüÊàêÁéØÂ¢ÉÂèòÈáèÊñá‰ª∂
  const envContent = `
# Generated by deploy.js at ${new Date().toISOString()}
NETWORK=${hre.network.name}
VERIFIER_ADDRESS=${verifierAddress}
ZKP_APPLICATION_ADDRESS=${zkpAppAddress}
`;

  const envPath = path.join(__dirname, "../.env.deployed");
  fs.writeFileSync(envPath, envContent.trim());
  console.log(`\n‚úÖ Environment variables saved to: ${envPath}`);
  console.log("\nüéâ Deployment complete!\n");
}

main()
  .then(() => process.exit(0))
  .catch((error) => {
    console.error("‚ùå Error:", error);
    process.exit(1);
  });
